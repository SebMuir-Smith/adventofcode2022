    private static class Head{

        int x;
        int y;

        Tail tail;


        public Head(Tail tail){
            x = 0;
            y = 0;
            this.tail = tail;
        }

        public void Move(String instruction){
            Matcher m = Pattern.compile("(.) (.*)").matcher(instruction);
            m.find();
            String dir = m.group(1);
            int nTimes = Integer.parseInt(m.group(2));

            int xDir = 0;
            int yDir = 0;

            switch (dir){
                case "U": yDir = 1;
                    break;
                case "D": yDir = -1;
                    break;
                case "L": xDir =-1;
                    break;
                case "R": xDir = 1;
                    break;
            }

            for (int i = 0; i < nTimes; i++){
                x += xDir;
                y += yDir;
                this.tail.HandleMove(x, y);
                //updateGrid(this);
            }


        }
    }

    private static class Tail{

        private int x;
        private int y;

        private int n;

        private Tail tail;
        boolean isTail;

        public Tail(Tail tail, boolean isTail, int n){
            x = 0;
            y = 0;
            this.tail = tail;
            this.isTail = isTail;
            this.n = n;
        }

        public void HandleMove(int headX, int headY){
            int xDiff = headX - x;
            int yDiff = headY - y;

            if (Math.abs(xDiff) > 1){
                x += xDiff/2;
                if (Math.abs(yDiff) > 0){
                    y += (Math.abs(yDiff) > 1 ? yDiff/2 : yDiff);
                }
            } else if (Math.abs(yDiff) > 1){
                y += yDiff/2;
                if (Math.abs(xDiff) > 0){
                    x += (Math.abs(xDiff) > 1 ? xDiff/2 : xDiff);
                }
            }

            if (this.isTail){
                System.out.println(x + "," + y);
                locations.add(x + "," + y);
            }

            if (this.tail != null){
                this.tail.HandleMove(x,y);
            }


        }
    }



